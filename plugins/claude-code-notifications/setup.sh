#!/bin/sh
# setup.sh — One-time configuration for claude-code-notifications
#
# Creates a .env file in the plugin directory with your ntfy settings.
# Run this after installing the plugin.

set -eu

PLUGIN_ROOT="$(cd "$(dirname "$0")" && pwd)"
ENV_FILE="${PLUGIN_ROOT}/.env"

echo "=== Claude Code Notifications — Setup ==="
echo ""

# ---- Server ----
printf "ntfy server URL [https://ntfy.sh]: "
read -r NTFY_SERVER
NTFY_SERVER="${NTFY_SERVER:-https://ntfy.sh}"
# Strip trailing slash
NTFY_SERVER="${NTFY_SERVER%/}"

# ---- Topic ----
DEFAULT_TOPIC="claude-notify-$(head -c 6 /dev/urandom | od -An -tx1 | tr -d ' \n')"
printf "ntfy topic name [%s]: " "$DEFAULT_TOPIC"
read -r NTFY_TOPIC
NTFY_TOPIC="${NTFY_TOPIC:-$DEFAULT_TOPIC}"

# ---- Auth token (optional) ----
echo ""
echo "If your ntfy server requires authentication, enter an access token."
echo "Leave blank for public/unauthenticated servers."
printf "ntfy access token []: "
read -r NTFY_TOKEN
NTFY_TOKEN="${NTFY_TOKEN:-}"

# ---- Priority ----
echo ""
echo "Notification priority: min, low, default, high, max"
printf "Priority [high]: "
read -r NTFY_PRIORITY
NTFY_PRIORITY="${NTFY_PRIORITY:-high}"

# ---- Write config ----
cat > "$ENV_FILE" <<EOF
# claude-code-notifications config — generated by setup.sh
NTFY_SERVER="${NTFY_SERVER}"
NTFY_TOPIC="${NTFY_TOPIC}"
NTFY_TOKEN="${NTFY_TOKEN}"
NTFY_PRIORITY="${NTFY_PRIORITY}"
EOF

echo ""
echo "Config written to: ${ENV_FILE}"
echo ""
echo "=== Next steps ==="
echo ""
echo "1. Install the ntfy app on your phone:"
echo "   iOS:     https://apps.apple.com/us/app/ntfy/id1625396347"
echo "   Android: https://play.google.com/store/apps/details?id=io.heckel.ntfy"
echo ""
if [ "$NTFY_SERVER" != "https://ntfy.sh" ]; then
    echo "2. In the ntfy app, add your self-hosted server: ${NTFY_SERVER}"
    echo "3. Subscribe to the topic: ${NTFY_TOPIC}"
else
    echo "2. In the ntfy app, subscribe to the topic: ${NTFY_TOPIC}"
fi
echo ""
echo "You can test the notification with:"
echo "  curl -H 'Title: Test' -d 'Hello from Claude Code' ${NTFY_SERVER}/${NTFY_TOPIC}"
echo ""
echo "Done!"
